        {
            scenes = new string[] { "Assets/Scenes/TerrainGenTest.unity", "Assets/Scenes/Credits.unity" },
            locationPathName = path + "/Windows/Smokejumpers.exe",
            target = BuildTarget.StandaloneWindows64,
        };
        var buildReport = BuildPipeline.BuildPlayer(buildOptions);


        if (buildReport.summary.result != BuildResult.Succeeded)
        {
            UnityEngine.Debug.Log("Windows build failed!\n\n" + buildReport.SummarizeErrors());
            return;
        }
        classContent = $@"
        // Auto-generated by BuildScript. Do not modify.
        namespace BuildSystem
        {{
            public static class BuildInfo
            {{
                public const string buildId = ""LNX{newGuid}"";
            }}
        }}";

        File.WriteAllText(Application.dataPath + "/BuildInfo.cs", classContent);
        buildOptions = new BuildPlayerOptions()
        {
