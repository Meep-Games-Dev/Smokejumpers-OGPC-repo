    [MenuItem("Build/Publish All")]
    public static void PublishAllBuilds()
    {
        string scriptPath = "/mnt/c/Users/ffkoe/OneDrive/Documents/Scripts/SmokejumpersBuildCompresser.sh";

        UnityEngine.Debug.Log($"Attempting to run WSL script at: {scriptPath}");
        ProcessStartInfo startInfo = new ProcessStartInfo();
        startInfo.FileName = "wsl.exe";
        startInfo.Arguments = $"'{scriptPath}' -r {Application.version} -y";
        startInfo.RedirectStandardOutput = true;
        startInfo.RedirectStandardError = true;
        startInfo.UseShellExecute = false;
        startInfo.CreateNoWindow = true;
        using (Process process = Process.Start(startInfo))
        {
            string output = process.StandardOutput.ReadToEnd();
            string error = process.StandardError.ReadToEnd();

            process.WaitForExit();

            if (!string.IsNullOrEmpty(output))
            {
                UnityEngine.Debug.Log($"WSL Output:\n{output}");
            }

            if (!string.IsNullOrEmpty(error))
            {
                UnityEngine.Debug.LogError($"WSL Error:\n{error}");
            }
        }
    }
